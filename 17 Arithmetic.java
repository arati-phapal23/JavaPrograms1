public class Arithmetic {
    public static void main(String[] args){
         int a = 4;
         int b = 5;

         System.out.println(a + b);
         System.out.println(a - b);
         System.out.println(a * b);
         System.out.println(a / b);
         System.out.println(a % b);


         double x = 7;
         double y = 3;

         System.out.println(x + y);
         System.out.println(x - y);
         System.out.println(x * y);
         System.out.println(x / y);
         System.out.println(x % y);



    }

}
